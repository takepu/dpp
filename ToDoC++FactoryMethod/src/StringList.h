#ifndef STRINGLIST_H_
#define STRINGLIST_H_

#include <string>
#include <vector>

namespace std {

class StringListIterator;

//---------------------------------------------------------------------------
// 文字列リスト
//---------------------------------------------------------------------------
class StringList {
public:
	//-----------------------------------------------------------------------
	// コンストラクタ
	//-----------------------------------------------------------------------
	StringList();

	//-----------------------------------------------------------------------
	// コピーコンストラクタ
	//-----------------------------------------------------------------------
	StringList( StringList *StringList );

	//-----------------------------------------------------------------------
	// デストラクタ
	//-----------------------------------------------------------------------
	virtual ~StringList();

	//-----------------------------------------------------------------------
	// 文字列追加
	//-----------------------------------------------------------------------
	void Add( string &str );

	//-----------------------------------------------------------------------
	// 文字列取得
	//-----------------------------------------------------------------------
	string& Get( unsigned long idx );

	//-----------------------------------------------------------------------
	// リスト長取得
	//-----------------------------------------------------------------------
	long GetLength();

	//-----------------------------------------------------------------------
	// イテレーター取得
	//-----------------------------------------------------------------------
	StringListIterator* GetIterator();

protected:
	vector <string> StrList;
	string NullStr;;
};
}
#endif /* STRINGLIST_H_ */
