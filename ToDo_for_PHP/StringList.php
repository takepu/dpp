<?php
	class StringList
	{
		//-----------------------------------------------------------------------
		// コンストラクタ
		//-----------------------------------------------------------------------
		public function __construct()
		{
			$this->str_list = array();
		}
	
		//-----------------------------------------------------------------------
		// 文字列追加
		//-----------------------------------------------------------------------
		public function Add( $str )
		{
			array_push( $this->str_list, $str );
			$this->GetLength();
		}
	
		//-----------------------------------------------------------------------
		// 文字列取得
		//-----------------------------------------------------------------------
		public function Get( $idx )
		{
			if( $idx >= $this->GetLength() )
			{
				return "";
			}
			return $this->str_list[$idx];
		}
	
		//-----------------------------------------------------------------------
		// リスト長取得
		//-----------------------------------------------------------------------
		public function GetLength()
		{
			return count( $this->str_list );
		}

		private $str_list;
	}
?>
