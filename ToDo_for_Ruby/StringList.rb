class StringList
    #-----------------------------------------------------------------------
    # コンストラクタ
    #-----------------------------------------------------------------------
    def Initialize()
    end
  
    #-----------------------------------------------------------------------
    # 文字列追加
    #-----------------------------------------------------------------------
    def Add( str )
        if @StrListObj.nil? then
            @StrListObj    = Array.new(1)
            @StrListObj[0] = str
        else
            @StrListObj << str
        end
    end
  
    #-----------------------------------------------------------------------
    # 文字列取得
    #-----------------------------------------------------------------------
    def Get( idx )
        if idx >= @StrListObj.length() then
            return ""
        end
        return @StrListObj[idx]
    end
  
    #-----------------------------------------------------------------------
    # リスト長取得
    #-----------------------------------------------------------------------
    def GetLength()
      return @StrListObj.length()
    end
end
