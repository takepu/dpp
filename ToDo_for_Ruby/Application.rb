require 'StringList'

class Application
    #-----------------------------------------------------------------------
    # コンストラクタ
    #-----------------------------------------------------------------------
    def Initialize()
        @StrList = StringList.new()
    end

    #-----------------------------------------------------------------------
    # 実行
    #---------------------------------------------------------------------------
    # 文字列をリストに登録する
    # コンソールから入力された文字列をリストに登録する。
    # [end]と入力すると終了。
    #---------------------------------------------------------------------------
    def Run()
        str = ""
        while true
            print("Please input your ToDo ([end]=exit) : ")
            str = gets().chomp()
            if( str == "end" ) then
                break
            end
            @StrList.Add( str )
        end
    end
  
    #-----------------------------------------------------------------------
    # 終了
    #-----------------------------------------------------------------------
    def Exit()
    end
end